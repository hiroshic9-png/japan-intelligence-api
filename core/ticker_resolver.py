_dynamic_cache = {}


def resolve_name(ticker: str) -> str:
    """ティッカーコードから会社名を解決する。静的辞書 → 動的取得の順。"""
    if ticker in TICKER_NAMES:
        return TICKER_NAMES[ticker]
    if ticker in _dynamic_cache:
        return _dynamic_cache[ticker]

    # Yahoo Finance Japanからフォールバック取得
    try:
        url = f"https://finance.yahoo.co.jp/quote/{ticker}"
        headers = {'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64)'}
        resp = requests.get(url, headers=headers, timeout=5)
        if resp.status_code == 200:
            match = re.search(r'^(.*?)【\d+】', resp.text[:500])
            if match:
                name = match.group(1).strip()
                _dynamic_cache[ticker] = name
                return name
    except Exception:
        pass

    code = ticker.replace('.T', '')
    _dynamic_cache[ticker] = code
    return code


def resolve_names_batch(tickers: list[str]) -> dict[str, str]:
    """複数ティッカーを一括解決。"""
    return {t: resolve_name(t) for t in tickers}
